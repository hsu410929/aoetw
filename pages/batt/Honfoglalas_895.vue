        <h2>攻略</h2>
        <div class="hrn1"></div>
        <div class="n1">
          <ul>
            <li>第一階段</li>
  <li>就如同上述的任務提示場景說明的，此關為特殊場景，需要配合劇情發展來進行下一個階段，
玩家可以視為是由多個小關卡組成的長篇章劇情戰役，如果玩家不配合劇情發展
劇情還是會到達一個臨界點來強制執行。</li>
<li>玩家將從封建時代開始，擁有少許軍隊和動物，但沒有村民和沒有建築物，除了幾個蒙古包帳篷。
開場佩切涅格人（黃色玩家）此時會用大量單位衝鋒， 所以不要試圖跟他們正面迎擊。而是盡快將所有單位向東移動，然後沿著下河直到他們到達卡札爾汗國（橘色）。</li>

<li>第二階段</li>
<li>到達卡札爾汗國後，卡札爾汗國王會配給玩家一塊陣地發展，將獲得一個擁有少數村民的市鎮中心。(但可以訓練牛羊) 此時生產更多村民並開始收集資源，
但不要在軍事單位上花太多黃金。這時只需防禦阻擋佩切涅格人(他們只會派輕騎兵和弓兵）
建造一些簡單的木牆和幾個箭塔，生產長槍兵與弓兵和矛兵，黃金成本較不易損耗，這時天然的金礦有限，省金為後面的發展鋪路
玩家在部落撤退時的狼隊也可來協同防禦城鎮
可以蓋市集生產一些貿易車來與您的的盟友卡札爾汗國進行交易。</li>
<li>在某些時刻（可能在達到一定人口數後），
佩切涅格人將發動一次大規模的攻擊，對方最多只會在一段時間內放慢速度，但是正常狀態此時玩家一般不會經受住他們的攻擊，
這時任務會提醒玩家帶領30個單位的人馬撤退到南部旗幟區。保存哪些單位完全不影響後續發展，因為劇情將將他們完全刪除。
這時玩家將自動升級城堡時代。</li>
<li>注意:玩家也可在此階段試著突破佩切涅格人的防線甚至深入對方陣地，但這需要更快的時間與技巧，佩切涅格人出兵到後續的一段時間後，會有一段空窗期，
生產近百位的軍隊(例如裝甲步兵可以快速的打擊對方建築)壓陣過去，但是即使玩家成功消滅佩切涅格人也不會勝利，更不會觸發後續階段的劇情。</li>

<li>第三階段</li>
<li>此時玩家將從西邊陣地開始會有7名部落酋長在城堡前宣示建盟完後，有一個穩定的定居點，包括三個城鎮中心和一座城堡，2間馬房與2間射箭場，一些馬札爾鏢騎與馬弓騎兵，
此階段你的宿敵佩切涅格人仍然是敵人，也與玩家一樣升級至城堡時代生產輕騎兵，弩兵與駱駝騎兵繼續向玩家發動進攻，但起初進攻單位並不多。
玩家可以善用城堡和城牆來防守。</li>
<li>記住玩家可以穿過卡札爾汗國的城門，這時玩家必須防守北部和東部的淺灘。
這邊陣地的天然資源較豐富且可以儲備力量（在這邊取得資源中獲得五位數並不難）。
據一些參考數據，有些玩家在這個階段成功打敗了佩切涅格人。
這是一項極其艱苦的作戰，但其實根本沒有必要。
就算玩家這樣做，只會影響關卡進程並無法獲得勝利。</li>

<li>第四階段</li>
<li>在某些時候，佩切涅格人將發動大規模攻擊，這次的單位數量將會比前個階段更多。但在他們到達玩家陣地之前，
此時拜占庭人將請求援助並要求玩家攻擊保加爾人，並且這時與佩切涅格人的外交狀態暫時為同盟。
這意味著他們的軍隊攻擊停滯不前，他們的一些單位可能會一直站在玩家的基地。
保加爾人實際上只攻擊最西邊的拜占庭城堡，直到他們的部隊用完為止，之後將不再進行進攻行動。</li>
<li>此時在這個時間點，玩家可藉由與佩切涅格人的同盟關係，生產了許多投石車，通過使用地形與無差別的範圍攻擊，慢慢的摧毀佩切涅格人
（例如，成群的單位等建築，所以他們不會阻止玩家的投石車攻擊，然後摧毀市中心，村民，軍事建築，軍事單位和其他部分建築）。
但是這樣娛樂的偷吃步，其實是沒必要的，再者，無論玩家做什麼都不會破壞劇情進行。</li>
<li>然而，玩家現在必須擊敗保加爾人。不要刪除毀掉運輸船，
它們將是重要的必需單位，而且此時無法生產的更多。保加爾人可以迅速生產出大量的單位:如斧槍兵，貴族鐵騎，投石車等，
所以如果玩家選擇在他們的領土上建立一個基地，則需要幾座城堡來抵擋敵人的猛攻。
玩家也可留意是否收集了足夠多的資源並刪除一部分村民騰出人口以建立一支龐大的軍隊。
生產許多炸藥筒並同時攻擊所有敵軍軍事建築和城鎮中心可能是個好主意。
這邊沒有必要殺死所有的保加爾人村民，他們便會投降。</li>
<li>第五階段</li>
<li>保加爾人在完全被擊敗之前就會投降，佩切涅格人與玩家的外交狀態從盟友轉回敵對，這將是大家所預測到情況
他們所有的單位都站在玩家的基地或附近領土（幾乎無法抵擋這時快速的突襲）
佩切涅格人會快速擊敗玩家，而此時玩家大多數軍隊都還在保加爾人的土地上。
其實沒有必要擔心，因為玩家將被賦予向西撤退的任務並將60單位送到那裡的旗幟。
同樣，要保存什麼樣的單位也都無關緊要，因為它們會被劇情自動刪除，以及玩家建造的一切陣地與建築。
這時玩家將獲得自動升級至帝王時代。</li>
<li>如果佩切涅格人之前被玩家以任何方式被徹底消滅，那麼在擊敗保加爾人之後情況可能形成關卡劇情被卡住。
無需擔心，這時只需刪除玩家開始使用的三個城鎮中心（參見第3階段）即可順利完成遊戲劇情進行。</li>
<li>第六階段</li>
<li>玩家的任務是將一名使者和一匹馬帶去給摩拉維亞人，稍微向西一點可以到達他們的陣地。 
當使者返回玩家的基地時，阿爾帕德就會向摩拉維亞人宣戰，此時玩家起步建立的基地將會比前面幾個階段來的容易，
應該擁有足夠的資源，因此劇情階段將會比其他階段相對容易。</li> 
                                           </ul>
