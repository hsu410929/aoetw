  為巴爾卡爾人與保加利亞人、楚瓦什人、塔塔爾族的先祖，為突厥人的一支（另一説法是來自帕米爾，與塔吉克人有關的東伊朗人），使用鄂爾渾文書寫他們的保加爾語
  (他們可能因受匈人統治而接受突厥語)，與今天的伏爾加韃靼人（中國的塔塔爾族大多為伏爾加韃靼）及楚瓦什人有聯繫（這支在伏爾加河中游建國）。
今天的保加利亞人已經斯拉夫化，他們的語言屬於斯拉夫語族。他們本來住在南俄，有自己汗國（古大保加利亞），
因受阿拉伯人與可薩人攻擊，分三支遷移。一支隨阿斯巴魯赫南遷至多瑙河下游小斯基提亞建國，另一支隨巴顏北遷至伏爾加河中游建立伏爾加保加利亞。
